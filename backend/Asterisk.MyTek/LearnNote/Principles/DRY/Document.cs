/* DRY
 * Every piece of knowledge must have a single, unambiguous(rõ ràng), authoritative(có thẩm quyền) representation(đại diện) 
within a system(trong một hệ thống).
example: you have class A để duyệt cây nhị phân -> it's a algorithm and define only one, but you can implement it in many classes.
-> đừng cài đặt một chức năng hai lần.

 * Lặp code và tái sử dụng code là 2 việc khác nhau:
DRY nói: bạn không nên để lặp tri thức.
DRY không nói: bạn nên viết theo kiểu tái sử dụng mọi thứ, viết 1 abstract class xong kế thừa thì có thể dính tight coupling(depend...)
 */